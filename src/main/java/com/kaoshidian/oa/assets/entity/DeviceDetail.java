package com.kaoshidian.oa.assets.entity;

import com.kaoshidian.oa.base.entity.BaseEntity;

public class DeviceDetail extends BaseEntity {
       
	  private Integer detailId;//ID
	  
	  private Integer deviceId;//设备ID
	  
	  private Integer usePersonId;//使用人
	  
	  private String  usePersonName;//使用者姓名

	  private Integer assignPersonId;// 分配人

	public Integer getDetailId() {
		return detailId;
	}

	public void setDetailId(Integer detailId) {
		this.detailId = detailId;
	}

	public Integer getDeviceId() {
		return deviceId;
	}

	public void setDeviceId(Integer deviceId) {
		this.deviceId = deviceId;
	}

	public Integer getUsePersonId() {
		return usePersonId;
	}

	public void setUsePersonId(Integer usePersonId) {
		this.usePersonId = usePersonId;
	}

	public Integer getAssignPersonId() {
		return assignPersonId;
	}

	public void setAssignPersonId(Integer assignPersonId) {
		this.assignPersonId = assignPersonId;
	}

	public String getUsePersonName() {
		return usePersonName;
	}

	public void setUsePersonName(String usePersonName) {
		this.usePersonName = usePersonName;
	}
	  
	  
}
