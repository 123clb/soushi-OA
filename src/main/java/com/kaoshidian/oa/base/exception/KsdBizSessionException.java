package com.kaoshidian.oa.base.exception;

/**
 * 如果已经确认登录成功，仍然无法在Session中得到当前用户则抛出此异常
 * @author <p>Innate Solitary 于 2012-3-27 上午11:20:51</p>
 *
 */
public class KsdBizSessionException extends KsdBizException {

	/**
     * 
     */
    private static final long serialVersionUID = 8513442103649243565L;

	public KsdBizSessionException() {
	    super();
    }

	public KsdBizSessionException(String message, Throwable cause) {
	    super(message, cause);
    }

	public KsdBizSessionException(String msg) {
	    super(msg);
    }

	public KsdBizSessionException(Throwable cause) {
	    super(cause);
    }
    
    

}
