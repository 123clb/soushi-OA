package com.kaoshidian.oa.base.exception;

/**
 * 加密解密发生错误时抛出此异常
 * @author <p>Innate Solitary 于 2012-8-6 下午4:12:42</p>
 *
 */
public class SecurityException extends KsdException {

	/**
     * 
     */
    private static final long serialVersionUID = -5780883866943555748L;

	public SecurityException() {
	    super();
    }

	public SecurityException(String message, Throwable cause) {
	    super(message, cause);
    }

	public SecurityException(String message) {
	    super(message);
    }

	public SecurityException(Throwable cause) {
	    super(cause);
    }

    
    
}
