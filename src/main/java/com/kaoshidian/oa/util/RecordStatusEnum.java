package com.kaoshidian.oa.util;

/**
 * 视频录制状态
 * @author <p>Innate Solitary 于 2012-10-7 上午11:27:42</p>
 *
 */
public enum RecordStatusEnum {
	PREPARE("准备中"), RECORDING("录制中"), COMPLETED("已完成"), UPLOADED("已上传");
	
	private String label;

	private RecordStatusEnum(String label) {
		this.label = label;
	}
	
	public String getLabel() {
		return this.label;
	}
}
